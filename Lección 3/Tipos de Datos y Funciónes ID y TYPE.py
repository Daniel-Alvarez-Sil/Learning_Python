#Enteros
x = 10
print("Dirección de Memoria", id(x))
print("Tipo de Dato", type(x))

#Decimales
x = 10.5
print("Dirección de Memoria", id(x))
print("Tipo de Dato", type(x))

#String
x = "¡Griffith!"
print("Dirección de Memoria", id(x))
print("Tipo de Dato", type(x))

#Logical
x = True
print("Dirección de Memoria", id(x))
print("Tipo de Dato", type(x))
x = False
print("Dirección de Memoria", id(x))
print("Tipo de Dato", type(x))