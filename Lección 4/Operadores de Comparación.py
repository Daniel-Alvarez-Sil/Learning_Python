x = 11
y = 11

lVar = x == y
print(f'X es igual a Y: {lVar}')

lVar = x != y
print(f'X no es igual a Y: {lVar}')

lVar = x > y
print(f'X es mayor que Y: {lVar}')

lVar = x >= y
print(f'X es mayor o igual que Y: {lVar}')

lVar = x < y
print(f'X es menor que Y: {lVar}')

lVar = x <= y
print(f'X es menor o igual que Y: {lVar}')


