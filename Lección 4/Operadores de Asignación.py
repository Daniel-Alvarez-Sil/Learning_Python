#Operador de Asignación Normal
iVar = 5
print(f'Variable con Valor Asignado a: {iVar}')

#Operadores de Reasignación
iVar += 1
print(f'Variable + 1: {iVar}')

iVar -= 1
print(f'Variable - 1: {iVar}')

iVar *= 2
print(f'Variable Multiplicada por 2: {iVar}')

iVar /= 2
print(f'Variable Dividida entre 2: {iVar}')

iVar **= 2
print(f'Variable a la 2da Potencia: {iVar}')

iVar %= 7
print(f'Residuo de la Variable Dividida entre 7: {iVar}')




